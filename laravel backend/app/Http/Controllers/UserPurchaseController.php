        $user_purchase->uid = $request->uid;
        $user_purchase->user_name = $request->user_name;
        $user_purchase->product_name = $request->product_name;
        $user_purchase->product_price = $request->product_price;
        if($user_purchase->save()){
            $message = "user purchase saved successfully";
        }else{
            $message = "User purchase did not saved successfully";
        }
        return response()->json([
            'messsage' => $message,
        ],200);
    }

